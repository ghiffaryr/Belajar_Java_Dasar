package com.tutorial;

public class Main {

    public static void main(String[] args){

        //Arithmetic Operation

        int var1 = 10;
        int var2 = 5;

        int result;

        //addition
        result = var1 + var2;
        System.out.println(var1 + " + " + var2 + " = " + result);

        //substraction
        result = var1 - var2;
        System.out.printf("%d - %d = %d \n",var1,var2,result);

        //multiplication
        result = var1 * var2;
        System.out.printf("%d * %d = %d \n",var1,var2,result);

        //division
        result = var1 / var2;
        System.out.printf("%d / %d = %d \n",var1,var2,result);

        float a = 6;
        float b = 5;
        float hasilFloat = a/b;
        System.out.println(a + " / " + b + " = " + hasilFloat);

        //mod
        result = var1 % var2;
        System.out.printf("%d / %d = %d \n",var1,var2,result);



    }
}
